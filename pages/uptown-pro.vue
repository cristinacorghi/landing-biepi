<script setup>
import { gsap } from 'gsap';
import ScrollTrigger from 'gsap/ScrollTrigger';
import { ref, onMounted } from "vue";

const presents = ref();
const title = ref();
const imageUptown = ref();
const uptownFrontale = ref();
const uptownRetro = ref();

onMounted(() => {

    gsap.registerPlugin(ScrollTrigger);

    gsap.fromTo(presents.value,
    {
        opacity: 0
    },
    {
        opacity: 1,
        duration: 2
    });

    var tl = gsap.timeline();

    tl.fromTo(imageUptown.value,
    {
        opacity: 0,
        yPercent: 5
    },
    {
        opacity: 1,
        yPercent: 0,
        duration: 2,
        ease: "power2.out"
    });

    tl.fromTo(title.value,
    {
        opacity: 0,
        yPercent: 90
    },
    {
        opacity: 1,
        yPercent: 0,
        duration: 2.5,
        ease: "power2.out"
    });

    gsap.fromTo(uptownFrontale.value,
    {
        opacity: 0,
        yPercent: 20
    },
    {
        opacity: 1,
        yPercent: 0,
        duration: 2,
        scrollTrigger: {
            trigger: uptownFrontale.value,
            start: 'top 80%',
            end: 'bottom 20%',
            scrub: true,
            once: true
        }
    });

    gsap.fromTo(uptownRetro.value,
    {
        opacity: 0,
        yPercent: 20
    },
    {
        opacity: 1,
        yPercent: 0,
        duration: 2,
        scrollTrigger: {
            trigger: uptownRetro.value,
            start: 'top 80%',
            end: 'bottom 20%',
            scrub: true,
            once: true
        }
    });
})
</script>

